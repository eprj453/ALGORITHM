import sys
sys.stdin = open('2_input.txt', 'r')


for i in range(1, int(input())+1):
    print('--------{}--------'.format(i))
    n, m = map(int, input().split())
    codes = [list(input()) for _ in range(n)]
    # print(codes)
    for j in range(n):
        for k in range(m):
            if codes[j][k] != '0':
                # print(j, k)
                x, y = j, k
                # print(x, y)
                ch = ''
                while True:
                    if y >= m:
                        break
                    if codes[x][y] == '0':
                        if y < m-3 and codes[x][y+1] == '0' and codes[x][y+2] == '0' and codes[x][y+3] == '0':
                            break
                    ch += str(codes[x][y])
                    y += 1
                print(ch)
                x, y = j, k
                while codes[x][y] != '0':
                    for l in range(y, y+len(ch)):
                        codes[x][l] = '0'
                    x += 1

    # print(ch)




